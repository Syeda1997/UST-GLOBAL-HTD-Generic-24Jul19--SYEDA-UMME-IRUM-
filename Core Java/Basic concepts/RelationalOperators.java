public class RelationalOperators{
	public static void main(String[] args) {
		char i='A';
		int k=66;
		boolean b=i==k;
		System.out.println("For ==: " +b);

		b=i!=k;
		System.out.println("For !=: " +b);

		b=i<k;
		System.out.println("For <: " +b);

		b=i>k;
		System.out.println("For >: " +b);

		b=i<=k;
		System.out.println("For <=: " +b);

		b=i>=k;
		System.out.println("For >=: " +b);
	}
}