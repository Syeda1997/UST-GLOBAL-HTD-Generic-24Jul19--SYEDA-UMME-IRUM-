public class CharOperators{
	public static void main(String[] args) {
		char i='a';
		char j='b';
		int res=i%j;//initialization of res variable
		System.out.println("Result = " +res);//modulus

		res=i/j;//reinitialization of res variable
		System.out.println("Result = " +res);//division

		res=i*j;//reinitialization of res variable
		System.out.println("Result = " +res);

		res=i-j;//reinitialization of res variable
		System.out.println("Result = " +res);

		res=i+j;//reinitialization of res variable
		System.out.println("Result = " +res);

	}
}