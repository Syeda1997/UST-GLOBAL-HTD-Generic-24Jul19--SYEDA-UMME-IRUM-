public class AssignmentOperator{
	public static void main(String[] args) {
		int i;
		int j;
		i=30;
		j=20 ;
		System.out.println("i = "+i);
		System.out.println("j = "+j);
		i+=j;//i=i+j
		System.out.println("i = "+i);
		i-=j;//i=i-j
		System.out.println("i = "+i);
		i*=j;//i=i*j
		System.out.println("i = "+i);
		i/=j;//i=i/j
		System.out.println("i = "+i);
		i%=j;//i=i%j
		System.out.println("i = "+i);
	}
}